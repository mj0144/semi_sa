package mvc.vo;

public class IdealVO {
	private int user_num;
	private int ideal_height;
	private int ideal_age;
	private int ideal_body;
	
	
	public int getUser_num() {
		return user_num;
	}
	public void setUser_num(int user_num) {
		this.user_num = user_num;
	}
	public int getIdeal_height() {
		return ideal_height;
	}
	public void setIdeal_height(int ideal_height) {
		this.ideal_height = ideal_height;
	}
	public int getIdeal_age() {
		return ideal_age;
	}
	public void setIdeal_age(int ideal_age) {
		this.ideal_age = ideal_age;
	}
	public int getIdeal_body() {
		return ideal_body;
	}
	public void setIdeal_body(int ideal_body) {
		this.ideal_body = ideal_body;
	}
	
}
